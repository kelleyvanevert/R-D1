package studie.callbydoodle;

public class Circle
{
	private float x, y, r;
	
	public Circle(float x, float y, float radius)
	{
		this.x = x;
		this.y = y;
		this.r = radius;
	}
	
	public float getX()
	{
		return x;
	}
	
	public float getY()
	{
		return y;
	}
	
	public float getRadius()
	{
		return r;
	}
}
